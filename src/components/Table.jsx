import React, { useContext } from 'react';
import DataContext from '../context/DataContext';

// Header values for the table
const TABLE_HEADER = ['Name', 'Rotation Period', 'Orbital Period', 'Diameter', 'Climate',
  'Gravity', 'Terrain', 'Surface Water', 'Population', 'Films', 'Created', 'Edited',
  'Url'];

function Table() {
  const { data } = useContext(DataContext);
  const { nameFilter } = useContext(DataContext);

  return (
    <table>
      <thead>
        <tr>
          {TABLE_HEADER.map((column) => (
            <th key={ column }>{column}</th>
          ))}
        </tr>
      </thead>
      {data.filter((planet) => (planet.name.toLowerCase())
        .includes(nameFilter.filterByName.name.toLowerCase()))
        .map((planet) => (
          <tbody key={ planet.name }>
            <tr>
              <td data-testid="planet-name">{ planet.name }</td>
              <td>{ planet.rotation_period }</td>
              <td>{ planet.orbital_period }</td>
              <td>{ planet.diameter }</td>
              <td>{ planet.climate }</td>
              <td>{ planet.gravity }</td>
              <td>{ planet.terrain }</td>
              <td>{ planet.surface_water }</td>
              <td>{ planet.population }</td>
              <td>{ planet.films }</td>
              <td>{ planet.created }</td>
              <td>{ planet.edited }</td>
              <td>{ planet.url }</td>
            </tr>
          </tbody>
        ))}
    </table>
  );
}

export default Table;
